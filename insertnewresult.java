    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String chemistry = jTextField3.getText();
        String maths = jTextField4.getText();
        String elective1 = jTextField5.getText();
        String elective2 = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/SRM", "root", "Shamala@0");
            Statement st  = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from students where rollno = '"+rollno+"'");
            if (rs.next()){
                st.executeUpdate("insert into result(rollno, physics, chemistry, mathematics, elective1, elective2) values( '"+rollno+"', '"+physics+"','"+chemistry+"','"+maths+"','"+elective1+"','"+elective2+"')");
                JOptionPane.showMessageDialog(null, "Sucessfully Updated");
                setVisible(false);
                new insertnewresult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Roll Number is does not exists in out Database");
                  setVisible(false);
                new insertnewresult().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

