    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        try{
               Class.forName("com.mysql.cj.jdbc.Driver");
               Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/SRM", "root", "Shamala@0");
               Statement st = con.createStatement();
               ResultSet rs = st.executeQuery("Select * from students inner join result where students.rollno = '"+rollno+"' and result.rollno = '"+rollno+"'");
               if (rs.next()){
                   setVisible(false);
                   new studenthome(rollno).setVisible(true);
                   
               }
               else{
                   JOptionPane.showMessageDialog(null, "Incorrect Roll Number");
               }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

